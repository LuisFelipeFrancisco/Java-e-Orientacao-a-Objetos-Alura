package Variables;

public class Converting{

    public static void main(String[] args){

        float floatingPoint = 3.14f;
        System.out.println(floatingPoint);

        double wage = 1270.50;
        int value = (int) wage;
        System.out.println(value);

        double value1 = 0.2;
        double value2 = 0.1;
        double total = value1 + value2;
        System.out.println(total);
    }
}
